
from aiogram import (
    Router,
    F
)

from aiogram.types import (
    Message
)

from utils.roles import (
    get_user_role
)


router = Router()


@router.message(
    F.text == "/faq"
)
async def faq_handler(
    message: Message
):

    role = get_user_role(
        message.from_user.id
    )

    # USER FAQ
    faq_text = (
        "❓ FAQ | Користувач\n\n"

        "📦 Як отримати техніку?\n"
        "• Натисніть 'Отримати техніку'\n"
        "• Виберіть техніку\n"

        "📤 Як здати техніку?\n"
        "• Натисніть 'Здати техніку'\n"
        "• Введіть поточні мотогодини\n\n"

        "🚜 Моя техніка\n"
        "• Показує активну техніку\n"
        "• Поточний час використання\n\n"

        "❌ Не можу зареєструватись\n"
        "• Перевірте номер телефону\n"
        "• Використовуйте номер із порталу\n"
        "• Зверніться до механіка\n\n"

        "🔒 Заблоковано отримання техніки\n"
        "• Зверніться до адміністратора"
    )

    # ADMIN FAQ
    if role == "admin":

        faq_text += (
            "\n\n"
            "🛠 FAQ | Адміністратор\n\n"

            "📋 Активна техніка\n"
            "• Перегляд всієї активної техніки\n"
            "• Контроль незданої техніки\n\n"

            "🔨 Бани\n"
            "• Заборона отримання техніки\n"
            "• Розблокування користувачів\n\n"

            "📊 Статистика\n"
            "• Журнал видачі техніки\n"
            "• Активність користувачів\n\n"

            "⚙️ Автоматизація\n"
            "• Автозакриття техніки\n"
            "• Нагадування користувачам"
        )

    # SUPER ADMIN FAQ
    elif role == "super_admin":

        faq_text += (
            "\n\n"
            "👑 FAQ | Super Admin\n\n"

            "👤 Користувачі\n"
            "• Список активних користувачів\n"
            "• Ручна реєстрація\n"
            "• Видалення користувачів\n"
            "• Скидання реєстрації\n\n"

            "🛡 Ролі\n"
            "• Видача ролей admin\n"
            "• Видача ролей super_admin\n"
            "• Пониження ролей\n\n"

            "📜 Логи\n"
            "• Перегляд логів системи\n"
            "• Очистка старих логів\n\n"

            "🔄 Синхронізація\n"
            "• Оновлення працівників із порталу\n"
            "• Видалення звільнених працівників"
        )

    await message.answer(
        faq_text
    )

